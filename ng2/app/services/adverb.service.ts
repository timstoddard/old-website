import { Injectable } from '@angular/core';

@Injectable()
export class AdverbService {
	get(firstLetter?: string) {
		if (firstLetter) {
			if (firstLetter.length > 1) {
				firstLetter = firstLetter.charAt(0);
			}
			let allStartingWithLetter = adverbs.filter(adjective => adjective.charAt(0) === firstLetter);
			return allStartingWithLetter[ Math.floor(Math.random() * allStartingWithLetter.length) ];
		}
		return adverbs[ Math.floor(Math.random() * adverbs.length) ];
	}
}

